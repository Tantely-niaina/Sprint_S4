package util;

public class VerbMethod {
    private String verb;

    private String Method;

    public  VerbMethod(){}


    public String getVerb() {
        return verb;
    }

    public void setVerb(String verb) {
        this.verb = verb;
    }

    public String getMethod() {
        return Method;
    }

    public void setMethod(String method) {
        Method = method;
    }
}
